    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        List<Entidades.Sesion> a = new SesionDao().listado();
        for (int i = 0; i < a.size(); i++) {
            String pass = DigestUtils.sha512Hex(jTextField2.getText());
            if (a.get(i).getUser().equals(jTextField1.getText()) && a.get(i).getPass().equals(pass)) {
                System.out.println("USUARIO CORRECTO");
                System.out.println("PASS CORRECTO");
                if (Sesion.w != a.get(i)) {
                    if (w != null) {
                        new SesionLog().UpdateSessionInactivo(w);
                    }
                }
                w = a.get(i);
                JOptionPane.showMessageDialog(null, "EL USUARIO ENTRO EXITOSAMENTE");
                new SesionLog().UpdateSessionActivo(a.get(i));
                new Activo().setVisible(true);
                super.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

